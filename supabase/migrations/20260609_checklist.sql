on conflict (id) do nothing;

-- ── Checklist Templates ──
create table if not exists public.checklist_templates (
  id bigint generated by default as identity primary key,
  outlet_id uuid not null references public.outlets(id) on delete cascade
    default '00000000-0000-0000-0000-000000000001',
  name text not null,
  checklist_type text not null check (checklist_type in (
    'opening', 'closing', 'kitchen_hygiene', 'stock_check', 'cash_closing'
  )),
  description text not null default '',
  assigned_role text not null check (assigned_role in (
    'owner', 'manager', 'kitchen', 'front_desk'
  )),
  is_recurring boolean not null default true,
  requires_verification boolean not null default true,
  is_active boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (outlet_id, checklist_type)
);

create index if not exists checklist_templates_type_idx
  on public.checklist_templates(checklist_type);

-- ── Checklist Template Runs ──
create table if not exists public.checklist_template_runs (
  id bigint generated by default as identity primary key,
  template_id bigint not null references public.checklist_templates(id) on delete cascade,
  run_key text not null,
  scheduled_time time not null,
  created_at timestamptz not null default now(),
  unique (template_id, run_key)
);

create index if not exists checklist_template_runs_template_idx
  on public.checklist_template_runs(template_id);

-- ── Checklist Template Items ──
create table if not exists public.checklist_template_items (
  id bigint generated by default as identity primary key,
  template_id bigint not null references public.checklist_templates(id) on delete cascade,
  description text not null,
  category text not null default 'General',
  sort_order integer not null default 0,
  is_critical boolean not null default false,
  requires_note_on_fail boolean not null default true,
  inventory_ref text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists checklist_template_items_template_idx
  on public.checklist_template_items(template_id, sort_order);

-- ── Checklist Instances ──
create table if not exists public.checklist_instances (
  id bigint generated by default as identity primary key,
  template_id bigint not null references public.checklist_templates(id) on delete cascade,
  outlet_id uuid not null references public.outlets(id) on delete cascade
    default '00000000-0000-0000-0000-000000000001',
  run_key text not null,
  business_date date not null,
  scheduled_time timestamptz not null,
  status text not null default 'pending' check (status in (
    'pending', 'in_progress', 'completed', 'verified'
  )),
  assigned_role text not null,
  completed_by uuid references public.staff_profiles(id),
  started_at timestamptz,
  completed_at timestamptz,
  verified_by uuid references public.staff_profiles(id),
  verified_at timestamptz,
  verification_note text,
  rejection_note text,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists checklist_instances_date_idx
  on public.checklist_instances(business_date);
create index if not exists checklist_instances_status_idx
  on public.checklist_instances(status, business_date);
create index if not exists checklist_instances_template_date_idx
  on public.checklist_instances(template_id, business_date);
create unique index if not exists checklist_instances_one_active_per_run
  on public.checklist_instances(template_id, business_date, outlet_id, run_key)
  where status in ('pending', 'in_progress');

-- ── Checklist Item Responses ──
create table if not exists public.checklist_item_responses (
  id bigint generated by default as identity primary key,
  instance_id bigint not null references public.checklist_instances(id) on delete cascade,
  template_item_id bigint not null references public.checklist_template_items(id) on delete cascade,
  status text not null default 'pending' check (status in (
    'pending', 'pass', 'fail', 'skip'
  )),
  note text,
  responded_by uuid references public.staff_profiles(id),
  responded_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (instance_id, template_item_id)
);

create index if not exists checklist_item_responses_instance_idx
  on public.checklist_item_responses(instance_id);

-- ═══════════════════════════════════════════════════════════════════
-- Auto-Scheduling Function
-- ═══════════════════════════════════════════════════════════════════

create or replace function public.schedule_daily_checklists(target_date date)
returns setof public.checklist_instances
language plpgsql
security definer
set search_path = public
as $$
declare
  run_record record;
  instance public.checklist_instances;
  existing_count integer;
  scheduled_ts timestamptz;
begin
  for run_record in
    select
      r.id as run_id,
      r.run_key,
      r.scheduled_time,
      t.id as template_id,
      t.assigned_role
    from public.checklist_template_runs r
    join public.checklist_templates t on t.id = r.template_id
    where t.is_active and t.is_recurring
  loop
    select count(*) into existing_count
    from public.checklist_instances
    where template_id = run_record.template_id
      and business_date = target_date
      and run_key = run_record.run_key
      and status in ('pending', 'in_progress');

    if existing_count = 0 then
      scheduled_ts := target_date + run_record.scheduled_time;

      insert into public.checklist_instances (
        template_id, run_key, business_date, scheduled_time,
        assigned_role
      ) values (
        run_record.template_id, run_record.run_key, target_date, scheduled_ts,
        run_record.assigned_role
      )
      returning * into instance;

      insert into public.checklist_item_responses (
        instance_id, template_item_id, status
      )
      select instance.id, ti.id, 'pending'
      from public.checklist_template_items ti
      where ti.template_id = run_record.template_id
      order by ti.sort_order;

      return next instance;
    end if;
  end loop;
end;
$$;

