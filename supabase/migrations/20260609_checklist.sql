-- Templates
create policy checklist_templates_read on public.checklist_templates
  for select to authenticated
  using (is_active or public.staff_role_is(array['owner', 'manager']));

create policy checklist_templates_manage on public.checklist_templates
  for all to authenticated
  using (public.staff_role_is(array['owner', 'manager']))
  with check (public.staff_role_is(array['owner', 'manager']));

create policy checklist_templates_delete on public.checklist_templates
  for delete to authenticated
  using (public.staff_role_is(array['owner']));

-- Template Runs — inherit from parent template
create policy checklist_template_runs_read on public.checklist_template_runs
  for select to authenticated
  using (
    exists (
      select 1 from public.checklist_templates t
      where t.id = template_id
        and (t.is_active or public.staff_role_is(array['owner', 'manager']))
    )
  );

-- Template Items
create policy checklist_template_items_read on public.checklist_template_items
  for select to authenticated
  using (
    exists (
      select 1 from public.checklist_templates t
      where t.id = template_id
        and (t.is_active or public.staff_role_is(array['owner', 'manager']))
    )
  );

create policy checklist_template_items_manage on public.checklist_template_items
  for all to authenticated
  using (public.staff_role_is(array['owner', 'manager']))
  with check (public.staff_role_is(array['owner', 'manager']));

-- Instances
create policy checklist_instances_read_all on public.checklist_instances
  for select to authenticated
  using (public.staff_role_is(array['owner', 'manager']));

create policy checklist_instances_read_role on public.checklist_instances
  for select to authenticated
  using (
    assigned_role = (
      select role from public.staff_profiles where id = auth.uid()
    )
  );

create policy checklist_instances_update_role on public.checklist_instances
  for update to authenticated
  using (
    assigned_role = (
      select role from public.staff_profiles where id = auth.uid()
    )
    and completed_by is null
  )
  with check (
    assigned_role = (
      select role from public.staff_profiles where id = auth.uid()
    )
  );

create policy checklist_instances_manage on public.checklist_instances
  for update to authenticated
  using (public.staff_role_is(array['owner', 'manager']))
  with check (public.staff_role_is(array['owner', 'manager']));

create policy checklist_instances_insert_system on public.checklist_instances
  for insert to authenticated
  with check (true);

-- Item Responses
create policy checklist_item_responses_read on public.checklist_item_responses
  for select to authenticated
  using (
    exists (
      select 1 from public.checklist_instances i
      where i.id = instance_id
        and (
          public.staff_role_is(array['owner', 'manager'])
          or i.assigned_role = (
            select role from public.staff_profiles where id = auth.uid()
          )
        )
    )
  );

create policy checklist_item_responses_respond on public.checklist_item_responses
  for all to authenticated
  using (
    exists (
      select 1 from public.checklist_instances i
      where i.id = instance_id
        and i.assigned_role = (
          select role from public.staff_profiles where id = auth.uid()
        )
        and i.completed_by is null
    )
  )
  with check (
    exists (
      select 1 from public.checklist_instances i
      where i.id = instance_id
        and i.assigned_role = (
          select role from public.staff_profiles where id = auth.uid()
        )
    )
  );

create policy checklist_item_responses_manage on public.checklist_item_responses
  for all to authenticated
  using (public.staff_role_is(array['owner', 'manager']))
  with check (public.staff_role_is(array['owner', 'manager']));

