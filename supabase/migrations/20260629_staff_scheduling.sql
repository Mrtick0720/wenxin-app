-- ── 4. team_on_duty(date) — privacy-safe "who is working" lookup ─────────────
-- SECURITY DEFINER: reads everyone's leave / shift / holiday data internally but
-- only returns the names + roles of staff resolved to "working" that date.
-- Mirrors the TS priority engine in lib/schedule/resolveScheduleStatus.ts —
-- keep the two in sync. Priority (highest → lowest):
--   approved leave → off · holiday+invited → work · holiday → off ·
--   manual shift override → its type · fixed weekly off → off · else → work.
create or replace function public.team_on_duty(target_date date)
returns table (staff_id uuid, display_name text, role text)
language sql
stable
security definer
set search_path = public
as $$
  select p.id, p.display_name, p.role
  from public.staff_profiles p
  where p.active = true
    and coalesce(p.archived, false) = false
    and p.role <> 'owner'
    and case
      when exists (
        select 1 from public.staff_leave_requests lr
        where lr.staff_id = p.id
          and lr.status = 'approved'
          and target_date between lr.start_date and lr.end_date
      ) then false
      when exists (
        select 1 from public.public_holidays h where h.holiday_date = target_date
      ) then exists (
        select 1 from public.public_holiday_work_invitations inv
        where inv.holiday_date = target_date and inv.staff_id = p.id
      )
      when exists (
        select 1 from public.staff_shifts s
        where s.staff_id = p.id and s.shift_date = target_date
      ) then (
        select s.shift_type not in ('off', 'leave')
        from public.staff_shifts s
        where s.staff_id = p.id and s.shift_date = target_date
        limit 1
      )
      when p.fixed_off_weekday = extract(dow from target_date)::int then false
      else true
    end
  order by p.display_name;
$$;

grant execute on function public.team_on_duty(date) to authenticated;

-- ── 5. Audit triggers (match existing operational-audit pattern) ─────────────
drop trigger if exists write_operational_audit on public.staff_leave_requests;
create trigger write_operational_audit
  after insert or update or delete on public.staff_leave_requests
  for each row execute function public.write_operational_audit();

drop trigger if exists write_operational_audit on public.public_holiday_work_invitations;
create trigger write_operational_audit
  after insert or update or delete on public.public_holiday_work_invitations
  for each row execute function public.write_operational_audit();

-- ── Validation ───────────────────────────────────────────────────────────────
do $$
begin
  if to_regclass('public.staff_leave_requests') is null
    or to_regclass('public.public_holidays') is null
    or to_regclass('public.public_holiday_work_invitations') is null then
    raise exception 'staff scheduling schema is incomplete';
  end if;
  if not exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'staff_profiles'
      and column_name = 'fixed_off_weekday'
  ) then
    raise exception 'fixed_off_weekday column missing on staff_profiles';
  end if;
end;
$$;

commit;
