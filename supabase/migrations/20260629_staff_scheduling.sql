-- Migration: Staff Scheduling — fixed weekly off day, leave requests,
--            public holidays + paid-leave work invitations.
-- ─────────────────────────────────────────────────────────────────────────────
-- Adds three capabilities to the Staff → Schedule module:
--   1. fixed_off_weekday on staff_profiles (owner-set recurring weekly off day)
--   2. staff_leave_requests (staff submit → owner/manager approve/reject)
--   3. public_holidays (paid leave by default) + public_holiday_work_invitations
--      (owner/manager invite specific staff to work on a holiday)
--
-- Plus public.team_on_duty(date): a SECURITY DEFINER helper that returns who is
-- scheduled to work on a given date, so normal staff can see "Today's Team On
-- Duty" WITHOUT being granted RLS read access to everyone's leave/schedule rows.
--
-- Re-runnable: guarded with IF NOT EXISTS / DROP POLICY IF EXISTS.
-- Targets the LIVE schema: staff_shifts uses (staff_id, shift_date, shift_type).
-- ─────────────────────────────────────────────────────────────────────────────

begin;

-- ── 1. Fixed weekly off day ──────────────────────────────────────────────────
-- Postgres dow convention: 0 = Sunday … 6 = Saturday. NULL = no fixed off day.
alter table public.staff_profiles
  add column if not exists fixed_off_weekday smallint
  check (fixed_off_weekday between 0 and 6);

-- ── 2. Leave requests ────────────────────────────────────────────────────────
create table if not exists public.staff_leave_requests (
  id          bigint generated by default as identity primary key,
  staff_id    uuid not null references public.staff_profiles(id) on delete cascade,
  start_date  date not null,
  end_date    date not null,
  reason      text not null default '',
  notes       text,
  status      text not null default 'pending'
              check (status in ('pending', 'approved', 'rejected', 'cancelled')),
  reviewed_by uuid references public.staff_profiles(id),
  reviewed_at timestamptz,
  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now(),
  check (end_date >= start_date)
);

create index if not exists staff_leave_requests_staff_status_idx
  on public.staff_leave_requests(staff_id, status);
create index if not exists staff_leave_requests_range_idx
  on public.staff_leave_requests(start_date, end_date);

alter table public.staff_leave_requests enable row level security;

-- Staff: read / create / cancel their own. Owner+manager: full manage.
drop policy if exists staff_leave_requests_read_own  on public.staff_leave_requests;
create policy staff_leave_requests_read_own on public.staff_leave_requests
  for select to authenticated
  using (staff_id = auth.uid());

drop policy if exists staff_leave_requests_manage_all on public.staff_leave_requests;
create policy staff_leave_requests_manage_all on public.staff_leave_requests
  for all to authenticated
  using (public.staff_role_is(array['owner', 'manager']))
  with check (public.staff_role_is(array['owner', 'manager']));

drop policy if exists staff_leave_requests_insert_own on public.staff_leave_requests;
create policy staff_leave_requests_insert_own on public.staff_leave_requests
  for insert to authenticated
  with check (staff_id = auth.uid() and status = 'pending');

-- Staff may only flip their own still-pending request to 'cancelled'.
drop policy if exists staff_leave_requests_cancel_own on public.staff_leave_requests;
create policy staff_leave_requests_cancel_own on public.staff_leave_requests
  for update to authenticated
  using (staff_id = auth.uid() and status = 'pending')
  with check (staff_id = auth.uid() and status = 'cancelled');

-- ── 3a. Public holidays ──────────────────────────────────────────────────────
create table if not exists public.public_holidays (
  id           bigint generated by default as identity primary key,
  holiday_date date not null unique,
  name         text not null,
  is_paid      boolean not null default true,
  created_at   timestamptz not null default now()
);

alter table public.public_holidays enable row level security;

drop policy if exists public_holidays_read_all on public.public_holidays;
create policy public_holidays_read_all on public.public_holidays
  for select to authenticated
  using (true);

drop policy if exists public_holidays_manage on public.public_holidays;
create policy public_holidays_manage on public.public_holidays
  for all to authenticated
  using (public.staff_role_is(array['owner', 'manager']))
  with check (public.staff_role_is(array['owner', 'manager']));

