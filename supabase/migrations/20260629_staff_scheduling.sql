-- ── 3b. Public holiday work invitations ──────────────────────────────────────
-- One row = this staff member is invited / eligible to work that public holiday.
create table if not exists public.public_holiday_work_invitations (
  id           bigint generated by default as identity primary key,
  holiday_date date not null,
  staff_id     uuid not null references public.staff_profiles(id) on delete cascade,
  invited_by   uuid references public.staff_profiles(id),
  created_at   timestamptz not null default now(),
