create or replace function public.write_auth_audit(
  audit_action text,
  audit_entity_type text,
  audit_entity_id text,
  audit_summary text,
  audit_before jsonb default null,
  audit_after jsonb default null
)
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  actor_id uuid := auth.uid();
  actor_staff text;
begin
  select staff_id into actor_staff
  from public.staff_profiles
  where id = actor_id;

  insert into public.audit_logs (
    actor_user_id,
    actor_staff_id,
    action,
    entity_type,
    entity_id,
    summary,
    before_data,
    after_data
  ) values (
    actor_id,
    actor_staff,
    left(audit_action, 80),
    left(audit_entity_type, 80),
    left(audit_entity_id, 160),
    left(audit_summary, 500),
    audit_before,
    audit_after
  );
end;
$$;

create or replace function public.start_staff_session(device_summary text default '')
returns public.staff_sessions
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  session_row auth.sessions%rowtype;
  profile_row public.staff_profiles%rowtype;
  result public.staff_sessions%rowtype;
begin
  select * into session_row
  from auth.sessions
  where id = public.current_session_id()
    and user_id = auth.uid();

  if session_row.id is null then
    raise exception 'Authentication session is unavailable';
  end if;

  select * into profile_row
  from public.staff_profiles
  where id = auth.uid();

  if profile_row.id is null or not profile_row.active then
    raise exception 'Staff account is unavailable';
  end if;

  insert into public.staff_sessions (
    id,
    staff_user_id,
    staff_id,
    started_at,
    last_seen_at,
    expires_at,
    device_summary
  ) values (
    session_row.id,
    profile_row.id,
    profile_row.staff_id,
    session_row.created_at,
    now(),
    session_row.created_at + interval '12 hours',
    left(coalesce(device_summary, ''), 240)
  )
  on conflict (id) do update
    set last_seen_at = now(),
        device_summary = excluded.device_summary
  returning * into result;

  update public.staff_profiles
  set last_login_at = now()
  where id = profile_row.id;

  perform public.write_auth_audit(
    'login',
    'staff_session',
    session_row.id::text,
    'Staff signed in'
  );

  return result;
end;
$$;

create or replace function public.touch_staff_session()
returns timestamptz
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  touched_at timestamptz;
begin
  if not public.is_current_staff_session_valid() then
    raise exception 'Staff session has ended';
  end if;

  update public.staff_sessions
  set last_seen_at = now()
  where id = public.current_session_id()
    and staff_user_id = auth.uid()
    and ended_at is null
    and last_seen_at < now() - interval '5 minutes'
  returning last_seen_at into touched_at;

  if touched_at is null then
    select last_seen_at into touched_at
    from public.staff_sessions
    where id = public.current_session_id()
      and staff_user_id = auth.uid();
  end if;

  return touched_at;
end;
$$;

create or replace function public.end_current_staff_session(reason text default 'logout')
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
begin
  if reason not in ('logout', 'expired') then
    raise exception 'Invalid session end reason';
  end if;

  update public.staff_sessions
  set ended_at = coalesce(ended_at, now()),
      end_reason = coalesce(end_reason, reason)
  where id = public.current_session_id()
    and staff_user_id = auth.uid();

  perform public.write_auth_audit(
    reason,
    'staff_session',
    coalesce(public.current_session_id()::text, auth.uid()::text),
    case when reason = 'expired' then 'Staff session expired' else 'Staff signed out' end
  );
end;
$$;

create or replace function public.complete_first_password_change()
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  required_at timestamptz;
  auth_updated_at timestamptz;
begin
  select password_change_required_at into required_at
  from public.staff_profiles
  where id = auth.uid()
    and active;

  select updated_at into auth_updated_at
  from auth.users
  where id = auth.uid();

  if required_at is null or auth_updated_at is null or auth_updated_at <= required_at then
    raise exception 'Password has not been changed';
  end if;

  update public.staff_profiles
  set must_change_password = false
  where id = auth.uid();

  perform public.write_auth_audit(
    'password_changed',
    'staff_profile',
    auth.uid()::text,
    'Staff changed the required password'
  );
end;
$$;

create or replace function public.invalidate_staff_sessions(
  target_user uuid,
  reason text default 'forced'
)
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  target_staff_id text;
begin
  if not public.staff_role_is(array['owner']) then
    raise exception 'Owner access required';
  end if;
  if reason not in ('forced', 'suspended') then
    raise exception 'Invalid session invalidation reason';
  end if;

  select staff_id into target_staff_id
  from public.staff_profiles
  where id = target_user;

  update public.staff_profiles
  set sessions_invalidated_at = now()
  where id = target_user;

  update public.staff_sessions
  set ended_at = coalesce(ended_at, now()),
      end_reason = coalesce(end_reason, reason)
  where staff_user_id = target_user
    and ended_at is null;

  delete from auth.sessions
  where user_id = target_user;

  perform public.write_auth_audit(
    case when reason = 'suspended' then 'account_suspended' else 'force_logout' end,
    'staff_profile',
    target_user::text,
    case
      when reason = 'suspended' then 'Owner suspended ' || coalesce(target_staff_id, 'staff account')
      else 'Owner forced logout for ' || coalesce(target_staff_id, 'staff account')
    end
  );
end;
$$;

create or replace function public.set_bento_order_status(
  order_id bigint,
  next_status text
)
returns void
language plpgsql
security definer
set search_path = public, auth
as $$
begin
  if not public.staff_role_is(array['owner', 'manager', 'kitchen']) then
    raise exception 'Bento fulfillment access required';
  end if;
  if next_status not in ('pending', 'completed') then
    raise exception 'Invalid fulfillment status';
  end if;

  update public.bento_orders
  set status = next_status
  where id = order_id;

  if not found then
    raise exception 'Bento order not found';
  end if;
end;
$$;

create or replace function public.write_operational_audit()
returns trigger
language plpgsql
security definer
set search_path = public, auth
as $$
declare
  old_data jsonb;
  new_data jsonb;
  entity_key text;
begin
  old_data := case when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old) else null end;
  new_data := case when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new) else null end;
  entity_key := coalesce(new_data ->> 'id', old_data ->> 'id');

  perform public.write_auth_audit(
    lower(tg_op),
    tg_table_name,
    entity_key,
    initcap(lower(tg_op)) || ' ' || replace(tg_table_name, '_', ' '),
    old_data,
    new_data
  );

  return case when tg_op = 'DELETE' then old else new end;
end;
$$;

