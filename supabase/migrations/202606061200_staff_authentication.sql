create or replace function public.current_session_id()
returns uuid
language sql
stable
security definer
set search_path = public, auth
as $$
  select case
    when coalesce(auth.jwt() ->> 'session_id', '') ~
      '^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$'
    then (auth.jwt() ->> 'session_id')::uuid
    else null
  end;
$$;

create or replace function public.is_current_staff_session_valid()
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $$
  select exists (
    select 1
    from public.staff_profiles p
    join auth.sessions s
      on s.user_id = p.id
     and s.id = public.current_session_id()
    where p.id = auth.uid()
      and p.active
      and s.created_at + interval '12 hours' > now()
      and (
        p.sessions_invalidated_at is null
        or s.created_at > p.sessions_invalidated_at
      )
  );
$$;

create or replace function public.current_staff_role()
returns text
language sql
stable
security definer
set search_path = public, auth
as $$
  select p.role
  from public.staff_profiles p
  where p.id = auth.uid()
    and p.active
    and public.is_current_staff_session_valid()
  limit 1;
$$;

create or replace function public.staff_role_is(allowed_roles text[])
returns boolean
language sql
stable
security definer
set search_path = public, auth
as $$
  select coalesce(public.current_staff_role() = any(allowed_roles), false);
$$;

