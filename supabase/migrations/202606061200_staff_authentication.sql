create table if not exists public.staff_profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  staff_id text not null unique check (staff_id ~ '^[a-z0-9][a-z0-9._-]{2,31}$'),
  display_name text not null,
  role text not null check (role in ('owner', 'manager', 'kitchen', 'front_desk')),
  active boolean not null default true,
  must_change_password boolean not null default true,
  password_change_required_at timestamptz not null default now(),
  sessions_invalidated_at timestamptz,
  created_by uuid references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  last_login_at timestamptz
);

create table if not exists public.staff_sessions (
  id uuid primary key,
  staff_user_id uuid not null references public.staff_profiles(id) on delete cascade,
  staff_id text not null,
  started_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  expires_at timestamptz not null,
  ended_at timestamptz,
  end_reason text check (end_reason in ('logout', 'expired', 'forced', 'suspended')),
  device_summary text not null default ''
);

create index if not exists staff_sessions_staff_user_idx
  on public.staff_sessions(staff_user_id, started_at desc);

create index if not exists staff_sessions_active_idx
  on public.staff_sessions(expires_at)
  where ended_at is null;

create table if not exists public.audit_logs (
  id bigint generated by default as identity primary key,
  actor_user_id uuid references auth.users(id) on delete set null,
  actor_staff_id text,
  action text not null,
  entity_type text not null,
  entity_id text,
  summary text not null,
  before_data jsonb,
  after_data jsonb,
  created_at timestamptz not null default now()
);

create index if not exists audit_logs_actor_created_idx
  on public.audit_logs(actor_user_id, created_at desc);

create index if not exists audit_logs_entity_created_idx
  on public.audit_logs(entity_type, created_at desc);

